#include <string.h>
#include <libc.h>

void	ft_strclr(char *s)
{
	char	*pointer;

	if (s)
	{
		pointer = s;
		while (*pointer != '\0')
		{
			*pointer = '\0';
			pointer++;
		}
	}
}
